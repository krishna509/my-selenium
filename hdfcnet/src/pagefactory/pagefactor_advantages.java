/*package pagefactory;


Page Object Pattern using PageFactory
Creating Selenium test cases can result 
in an un-maintainable project.
 One of the reasons is that too many duplicated code is used.
  Duplicated code could be caused by duplicated functionality and this will result in duplicated usage of locators.
 The disadvantage of duplicated code is that the project is less maintainable.
 If some locator will change, you have to walk through the whole test code to adjust locators where necessary.
  By using the Page Object Pattern using PageFactory we can make non-brittle test code and reduce or eliminate duplicate test code.
  Beside of that it improves the readability and allows us to create interactive documentation.
   Last but not least, we can create tests with less keystroke. 
   An implementation of the page object model can be achieved by separating the abstraction of the test object 
   and the test scripts.

 

Advantages of using Page Object Pattern:
Easy to Maintain
Easy Readability of scripts
Reduce or Eliminate duplicacy
Re-usability of code
Reliability
*/
	


